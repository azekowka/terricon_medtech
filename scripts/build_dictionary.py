def build():
    out = []
    for code, name, category, synonyms, base_price, duration in SERVICES:
        out.append({
            "code": code,
            "name": name,
            "category": category,
            "category_label": CATEGORIES[category],
            "synonyms": synonyms,
            "base_price_kzt": base_price,
            "duration_days": duration,
        })
    # sanity: unique codes
    codes = [s["code"] for s in out]
    assert len(codes) == len(set(codes)), "duplicate service codes"
    root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    path = os.path.join(root, "data", "services_dictionary.json")
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(out, f, ensure_ascii=False, indent=2)
    by_cat = {}
    for s in out:
        by_cat[s["category"]] = by_cat.get(s["category"], 0) + 1
    print(f"Wrote {len(out)} services to {path}")
    print("By category:", by_cat)


if __name__ == "__main__":
    build()
